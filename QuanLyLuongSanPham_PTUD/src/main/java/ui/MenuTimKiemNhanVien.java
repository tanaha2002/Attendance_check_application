    private void themSuKien() {
    	btnTimKiemNhanVien.setEnabled(false);
    	pnlBodyTim.add(new TimKiemNV());
    	
    	btnTimKiemChamCongNV.addActionListener(new ActionListener() {
			
			@Override
			public void actionPerformed(ActionEvent e) {
				btnTimKiemChamCongNVActionPerformed(e);
				
			}
			private void btnTimKiemChamCongNVActionPerformed(ActionEvent evt) {
				pnlBodyTim.removeAll();
				pnlBodyTim.add(new TimKiemChamCongNhanVien());
				pnlBodyTim.repaint();
				pnlBodyTim.revalidate();
				btnTimKiemChamCongNV.setEnabled(false);
				btnTimKiemNhanVien.setEnabled(true);
			}
			
		});
    	
    	btnTimKiemNhanVien.addActionListener(new ActionListener() {
			
			@Override
			public void actionPerformed(ActionEvent e) {
				btnTimKiemNhanVienActionPerformed(e);
				
			}
			private void btnTimKiemNhanVienActionPerformed(ActionEvent evt) {
				pnlBodyTim.removeAll();
				pnlBodyTim.add(new TimKiemNV());
				pnlBodyTim.repaint();
				pnlBodyTim.revalidate();
				btnTimKiemNhanVien.setEnabled(false);
		    	btnTimKiemChamCongNV.setEnabled(true);
			}
			
		});
    }

