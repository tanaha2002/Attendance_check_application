	private String maChamCongMoi(String maChamCong) {
		String maChamCongMoi = null;
		if (maChamCong == null) {
			maChamCongMoi = "CC000001";

		} else {
			int number = Integer.parseInt(maChamCong.substring(2));
			number++;
			maChamCongMoi = String.format("CC%06d", number);
			// System.out.println(maChamCongMoi);
		}

		return maChamCongMoi;
	}

