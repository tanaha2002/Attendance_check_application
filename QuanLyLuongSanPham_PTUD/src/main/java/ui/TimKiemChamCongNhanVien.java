			private List<BangChamCongNhanVien> layDanhSachChamCongTimKiem(int kieuTimKiem) {
				String maNV = txtMaNhanVien.getText().isEmpty() ? null : txtMaNhanVien.getText().trim();
				String tenNV = txtTenNhanVien1.getText().trim().isEmpty() ? null : txtTenNhanVien1.getText().trim();
				String maNguoiChamCong = txtMaNguoiChamCong.getText().isEmpty() ? null
						: txtMaNguoiChamCong.getText().trim();
				String tenNguoiChamCong = txtTenNguoiChamCong.getText().isEmpty() ? null
						: txtTenNguoiChamCong.getText().trim();
				Date ngayChamCong = null;
				Date denNgay = null;
				String soGioLamThem = txtSoGioLamThem.getText().isEmpty() ? null : txtSoGioLamThem.getText().trim();
				String tinhTrang = cmbTinhTrang.getSelectedItem().toString().trim().equalsIgnoreCase("All") ? null
						: cmbTinhTrang.getSelectedItem().toString().trim();
				String tinhTrangCong = cmbTinhTrangCong.getSelectedItem().toString().trim().equalsIgnoreCase("All")
						? null
						: cmbTinhTrangCong.getSelectedItem().toString().trim();
				try {
					ngayChamCong = dtNgayChamCong.getDate();
					denNgay = dtNgayChamCong1.getDate();

				} catch (Exception e) {
					e.printStackTrace();
					
				}
				try {
					return chamCongNhanVienDAO.timKiemChamCongNhieuThuocTinh(maNV, tenNV, ngayChamCong,denNgay, soGioLamThem,
							tinhTrang, maNguoiChamCong, tenNguoiChamCong, tinhTrangCong, kieuTimKiem);
				} catch (Exception e) {
					
					e.printStackTrace();
				}
				return null;

			};

